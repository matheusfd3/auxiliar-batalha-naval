function afundarBarco(barco) {
	switch(barco) {
		case "barcoDois":
			barcos.barcoDois = false;
			break;
		case "barcoTres":
			barcos.barcoTres = false;
			break;
		case "barcoTresUm":
			barcos.barcoTresUm = false;
			break;
		case "barcoQuatro":
			barcos.barcoQuatro = false;
			break;
		case "barcoCinco":
			barcos.barcoCinco = false;
			break;
		case "barcoTresTres":
			barcos.barcoTresTres = false;
			break;
	}
}